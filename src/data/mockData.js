const cleanUrl = (value) => {
  if (typeof value !== "string") return null;
  const trimmed = value.trim();
  return trimmed.length ? trimmed : null;
};

export const carImageSet = (_make, _model, _year, images = {}) => {
  const mainImage = cleanUrl(images.main) || PLACEHOLDER_IMAGE;
  const exteriorImages = Array.isArray(images.exterior)
    ? images.exterior.map(cleanUrl).filter(Boolean)
    : [];
  const interiorImages = Array.isArray(images.interior)
    ? images.interior.map(cleanUrl).filter(Boolean)
    : [];

  return { mainImage, exteriorImages, interiorImages };
};

const createCar = ({
  id,
  make,
  model,
  year,
  price,
  location,
  condition = "Used",
  color,
  mileageKm,
  transmission,
  fuel,
  drivetrain,
  engine,
  bodyType,
  seats,
  doors,
  registered,
  images,
  features,
}) => {
  const imgs = carImageSet(make, model, year, images);
  return {
    id,
    type: "car",
    title: `${make} ${model} ${year}${color ? ` (${color})` : ""}`,
    price,
    location,
    condition,
    year,
    make,
    model,
    image: imgs.mainImage,
    mainImage: imgs.mainImage,
    exteriorImages: imgs.exteriorImages,
    interiorImages: imgs.interiorImages,
    extraImages: [...imgs.exteriorImages, ...imgs.interiorImages],
    specs: {
      color: color || null,
      mileageKm: typeof mileageKm === "number" ? mileageKm : null,
      transmission: transmission || null,
      fuel: fuel || null,
      drivetrain: drivetrain || null,
      engine: engine || null,
      bodyType: bodyType || null,
      seats: typeof seats === "number" ? seats : null,
      doors: typeof doors === "number" ? doors : null,
      registered: typeof registered === "boolean" ? registered : null,
    },
    features: Array.isArray(features) ? features : [],
  };
};

